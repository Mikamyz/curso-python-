# Crie um código onde o usuário deve inserir dois números e exiba a soma, subtração, multiplicação e divisão deles.
num1 = int((input("Digite um número: ")))
num2 = int((input("Digite um número: ")))

print(f"Soma de {num1} e {num2} é: {num1 + num2}")
print(f"Subtração de {num1} e {num2} {num1 - num2}")
print(f"Multiplicação de {num1} e {num1 * num2}")
print(f"Divisão de {num1} e {num2} {num1 // num2}")

